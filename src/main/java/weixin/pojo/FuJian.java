public class FuJian {

    private String fileName;
    private String filePath;
    private String serverPathHead;
    private String serverPathBody;
    private String fullPath;

    public String getFileName() {
        return fileName;
    }

    public void setFileName(String fileName) {
        this.fileName = fileName;
    }

    public String getFilePath() {
        return filePath;
    }

    public void setFilePath(String filePath) {
        this.filePath = filePath;
    }

    public String getServerPathHead() {
        return serverPathHead;
    }

    public void setServerPathHead(String serverPathHead) {
        this.serverPathHead = serverPathHead;
    }

    public String getServerPathBody() {
        return serverPathBody;
    }

    public void setServerPathBody(String serverPathBody) {
        this.serverPathBody = serverPathBody;
    }

    public String getFullPath() {
        return fullPath;
    }

    public void setFullPath(String fullPath) {
        this.fullPath = fullPath;
    }
}
